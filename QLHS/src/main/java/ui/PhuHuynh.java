    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    StringBuilder sb = new StringBuilder();
    DataValidator.validateEmpty(txtMaHS, sb, "Mã học sinh cần nhập");
    if (sb.length() > 0) {
        MessageQLHSDialog.showErrorDialog(parentForm, sb.toString(), "Lỗi");
        return;
    }
    try {
        Phuhuynh ph = new Phuhuynh();
        ph.setMaHS(txtMaHS.getText());
        ph.setTencha(txtTencha.getText());
        ph.setSdtcha(txtSDTcha.getText());
        ph.setNncha(txtNNcha.getText());
        ph.setTenme(txtTenme.getText());
        ph.setSdtme(txtSDTme.getText());
        ph.setNnme(txtNNme.getText());
        
        PhuHuynhDao dao = new PhuHuynhDao();

        if (dao.findmaHS(txtMaHS.getText()) != null) {
            if (MessageQLHSDialog.showConfirmDialog(parentForm, "Bạn có muốn lưu thông tin phụ huynh không", "Hỏi") == JOptionPane.YES_OPTION) {
                return;
            }
            if (dao.update(ph)) {
                MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin phụ huynh đã được lưu", "Thông báo");
            } else {
                MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin phụ huynh không được lưu do lỗi", "Cảnh báo");
            }
        } else {
            if (dao.insert(ph)) {
                MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin phụ huynh đã được lưu", "Thông báo");
            } else {
                MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin phụ huynh được lưu do lỗi", "Cảnh báo");
            }
        }
        loadDataTable();
    }catch(Exception e) { 
        e.printStackTrace();
        MessageQLHSDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
    }
    }//GEN-LAST:event_btnSaveActionPerformed

