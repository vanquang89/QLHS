    private void loadDataTable(){
        try{
            PhuHuynhDao dao = new PhuHuynhDao();
            List<Phuhuynh> list = dao.findAll();
            tblModel.setRowCount(0);
            for (Phuhuynh it : list){
                tblModel.addRow(new Object[]{
                        it.getMaHS(), it.getTencha(), it.getSdtcha(), it.getNncha(), it.getTenme(), it.getSdtme(), it.getNnme()
                        });
            }tblModel.fireTableDataChanged();   
        }catch(Exception e){
            e.printStackTrace();
            MessageQLHSDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }

