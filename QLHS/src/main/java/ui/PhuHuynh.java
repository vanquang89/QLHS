    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         try{
            if (txtMaHS.getText().equals("")) {
                MessageQLHSDialog.showMessageDialog(parentForm, "Mã học sinh cần phải nhập", "Lỗi");
                return;
            }

            PhuHuynhDao dao = new PhuHuynhDao();
            Phuhuynh ph = dao.findmaHS(txtMaHS.getText());

            if(ph==null){
                MessageQLHSDialog.showMessageDialog(parentForm,"Không tìm thấy thông tin phụ huynh học sinh cần xóa" , "Lỗi");
                return;
            }
            if(MessageQLHSDialog.showConfirmDialog(parentForm, "Bạn có muốn xóa thông tin phụ huynh của học sinh không?", "Hỏi") == JOptionPane.NO_OPTION){
                return;
            }
            if(dao.delete(txtMaHS.getText())){
                MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin phụ huynh của học sinh đã bị xóa" , "Thông báo");
            }else{
                MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin phụ huynh của học sinh không bị xóa" , "Lỗi");
            }
            loadDataTable();
        }catch(Exception e){
            e.printStackTrace();
            MessageQLHSDialog.showMessageDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


