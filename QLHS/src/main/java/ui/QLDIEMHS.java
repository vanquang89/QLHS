    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
    DataValidator.validateEmpty(txtMaHS, sb, "Mã học sinh cần nhập");
    if (sb.length() > 0) {
        MessageQLHSDialog.showErrorDialog(parentForm, sb.toString(), "Lỗi");
        return;
    }
    try {
        BangDiem bd = new BangDiem();
        bd.setMaHS(txtMaHS.getText());
        bd.setToan(Float.parseFloat(txtToan.getText()));
        bd.setVan(Float.parseFloat(txtVan.getText()));
        bd.setAnh(Float.parseFloat(txtAnh.getText()));
        bd.setLy(Float.parseFloat(txtLy.getText()));
        bd.setHoa(Float.parseFloat(txtHoa.getText()));
        bd.setSinh(Float.parseFloat(txtSinh.getText()));
        bd.setSu(Float.parseFloat(txtSu.getText()));
        bd.setDia(Float.parseFloat(txtDia.getText()));
        bd.setGDCD(Float.parseFloat(txtGDCD.getText()));
        BangDiemDao dao = new BangDiemDao();

        if (dao.findbyMaHS(txtMaHS.getText()) != null) {
            if (MessageQLHSDialog.showConfirmDialog(parentForm, "Bạn có muốn lưu bảng điểm không", "Hỏi") == JOptionPane.YES_OPTION) {
                return;
            }
            if (dao.update(bd)) {
                MessageQLHSDialog.showMessageDialog(parentForm, "Bảng điểm đã được lưu", "Thông báo");
            } else {
                MessageQLHSDialog.showMessageDialog(parentForm, "Bảng điểm không được lưu do lỗi", "Cảnh báo");
            }
        } else {
            if (dao.insert(bd)) {
                MessageQLHSDialog.showMessageDialog(parentForm, "Bảng điểm đã được lưu", "Thông báo");
            } else {
                MessageQLHSDialog.showMessageDialog(parentForm, "Bảng điểm không được lưu do lỗi", "Cảnh báo");
            }
        }
        loadDataTable();
    } catch (Exception e) {
        e.printStackTrace();
        MessageQLHSDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
    }
    }//GEN-LAST:event_btnSaveActionPerformed

