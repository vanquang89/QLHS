    private void txtToanFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtToanFocusLost
        if (txtToan.getText().equals("")
                || txtVan.getText().equals("")
                || txtAnh.getText().equals("")
                || txtLy.getText().equals("")
                || txtHoa.getText().equals("")
                || txtSinh.getText().equals("")
                || txtSu.getText().equals("")
                || txtDia.getText().equals("")
                || txtGDCD.getText().equals("")) {
            return;
        }

        float Toan = Float.parseFloat(txtToan.getText());
        float Van = Float.parseFloat(txtVan.getText());
        float Anh = Float.parseFloat(txtAnh.getText());
        float Ly = Float.parseFloat(txtLy.getText());
        float Hoa = Float.parseFloat(txtHoa.getText());
        float Sinh = Float.parseFloat(txtSinh.getText());
        float Su = Float.parseFloat(txtSu.getText());
        float Dia = Float.parseFloat(txtDia.getText());
        float GDCD = Float.parseFloat(txtGDCD.getText());

        float avg = (Toan + Van + Anh + Ly + Hoa + Sinh + Su + Dia + GDCD) / 9;

        String st = String.format("%.2f", avg);
        lblDTB.setText(st);
    }//GEN-LAST:event_txtToanFocusLost

