    private void loadDataTable(){
        try{
            BangDiemDao dao = new BangDiemDao();
            List<BangDiem> list = dao.findAll();
            tblModel.setRowCount(0);
            for (BangDiem it : list){
                tblModel.addRow(new Object[]{
                        it.getMaHS(), it.getToan(), it.getVan(), it.getAnh(), it.getLy(), it.getHoa(), it.getSinh(),it.getSu(),it.getDia(),it.getGDCD()
                        });
            }tblModel.fireTableDataChanged();   
        }catch(Exception e){
            e.printStackTrace();
            MessageQLHSDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }

