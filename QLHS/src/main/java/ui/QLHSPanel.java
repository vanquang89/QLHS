    private void tblHocSinhMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHocSinhMouseClicked
        try { 
            int row = tblHocSinh.getSelectedRow();
            if(row>=0){ 
                String id = (String) tblHocSinh.getValueAt(row, 0);
                HocSinDao dao = new HocSinDao();
                HocSinh hs = dao.findmaHS(id);
                if(hs!=null){
                    txtMaHS.setText(hs.getMaHS());
                    txtTen.setText(hs.getTenHS());
                    txtLop.setText(hs.getLop());
                    txtEmail.setText(hs.getEmail());   
                    txtNgaySinh.setText(hs.getNgaysinh());
                    rdbNam.setSelected(hs.getGioitinh()==1?true:false);
                    txtDiachi.setText(hs.getDiachi());
                }
            }
        }catch(Exception e){ 
            e.printStackTrace();
            MessageQLHSDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblHocSinhMouseClicked

