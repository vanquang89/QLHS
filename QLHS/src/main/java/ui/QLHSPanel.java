    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaHS, sb, "Mã học sinh");
        if(sb.length()>0){ 
            MessageQLHSDialog.showErrorDialog(parentForm,sb.toString() ,"Lỗi" );
            return;
        }
        if(MessageQLHSDialog.showConfirmDialog(parentForm, "Bạn có muốn xóa thông tin học sinh không?", "Hỏi")==JOptionPane.NO_OPTION){
            return;
        }
        try {
           HocSinDao dao = new HocSinDao();
           if(dao.delete(txtMaHS.getText())) { 
                MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin của học sinh đã được xóa", "Thông báo");
           }else{
               MessageQLHSDialog.showMessageDialog(parentForm, "Thông tin của học sinh chưa được xóa do lỗi", "Cảnh báo");
           }loadDataTable();
        }catch(Exception e) { 
            e.printStackTrace();
            MessageQLHSDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

