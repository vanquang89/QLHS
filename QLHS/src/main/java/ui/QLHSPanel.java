    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtMaHS, sb, "Mã học sinh");
        DataValidator.validateEmpty(txtTen, sb, "Tên học sinh");
        if(sb.length()>0){ 
            MessageQLHSDialog.showErrorDialog(parentForm,sb.toString() ,"Lỗi" );
            return;
        }
        if(MessageQLHSDialog.showConfirmDialog(parentForm, "Bạn có muốn cập nhật lại thông tin học sinh không?","Hỏi")==JOptionPane.NO_OPTION){
            return;
        }
        try { 
            HocSinh hs = new HocSinh();
            hs.setMaHS(txtMaHS.getText());
            hs.setTenHS(txtTen.getText());
            hs.setLop(txtLop.getText());
            hs.setEmail(txtEmail.getText());
            hs.setNgaysinh(txtNgaySinh.getText());
            hs.setGioitinh(rdbNam.isSelected()?1:0);
            hs.setDiachi(txtDiachi.getText());
            
           HocSinDao dao = new HocSinDao();
           if(dao.update(hs)) { 
                MessageQLHSDialog.showMessageDialog(parentForm, "Học sinh đã được cập nhật", "Thông báo");
           }else{
               MessageQLHSDialog.showMessageDialog(parentForm, "Học Sinh không được cập nhật do lỗi", "Cảnh báo");
           }
           loadDataTable();
        }catch(Exception e) { 
            e.printStackTrace();
            MessageQLHSDialog.showErrorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

